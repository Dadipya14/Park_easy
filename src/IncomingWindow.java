    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    SessionFactory sf=new Configuration().configure().buildSessionFactory();
    Session session=sf.openSession();
    Transaction tx=session.beginTransaction();
    
    String vehicleId=jTextField1.getText();
    String numberPlate= jTextField2.getText();
    String type=(String)jComboBox1.getSelectedItem();
    String entryTime=jTextField4.getText();
    String exitTime=jTextField5.getText();
    String slotNum=jTextField3.getText();
    
    Vehicle veh=new Vehicle(Integer.parseInt(vehicleId),Integer.parseInt(numberPlate),Integer.parseInt(type),entryTime,exitTime,Integer.parseInt(slotNum));
    session.save(veh);
    tx.commit();
    session.close();
    JOptionPane.showMessageDialog(this,"DONE");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    SessionFactory sf=new Configuration().configure().buildSessionFactory();
    Session session=sf.openSession();
    Transaction tx=session.beginTransaction();
    
    String vehicleId=jTextField15.getText();
    String mode=jTextField16.getText();
    String amount=jTextField17.getText();
    String numberPlate=jTextField18.getText();
    DateModel dm = jDatePicker1.getModel();
    String date = dm.getDay()+" "+dm.getMonth()+" "+dm.getYear();
    String time=jTextField19.getText();
    TransactionDetails td =new TransactionDetails(Integer.parseInt(vehicleId),mode,Integer.parseInt(amount),Integer.parseInt(numberPlate),date,time);
    
    session.save(td);
    tx.commit();
    session.close();
    JOptionPane.showMessageDialog(this,"DONE");
    dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

